
# Operadores Aritméricos

valor1 = int(input('Digite um valor:'))
valor2 = int(input('Digite outro valor: '))

soma = valor1 + valor2
multiplicacao = valor1 * valor2
divisao = valor1 / valor2
divisaoInteira = valor1 // valor2
elevado = valor1 ** valor2

print(f'A soma é {soma}, \n o produto é {multiplicacao} \n e a divisão é {divisao}')
print('Divisão inteira {} \n e potência {}'.format(divisaoInteira, elevado))

