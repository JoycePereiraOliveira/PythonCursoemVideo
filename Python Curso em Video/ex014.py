
# Conversor de Temperatuas

temperatura = float(input('Temperatura em ºC:  \n'))

fahrenheit = (temperatura * 1.8) + 32

print('A conversão de Celsius para Fanrenheit é {:.0f}º \n'.format(fahrenheit))

