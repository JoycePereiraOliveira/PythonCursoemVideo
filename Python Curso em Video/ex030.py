
# Par ou Ímpar

numeroInteiro = int(input('Digite um número inteiro: '))
inteiro = numeroInteiro % 2;
if inteiro == 0:
    print('{} é um número par".format(numeroInteiro')
else:
    print('É um número ímpar')

